var findMaxConsecutiveOnes = function(nums) {
    var count = 0;
    var resArr = [];
    var maxRes = 0;

    for (var i = 0; i < nums.length; i++) {
        if (nums[i] == 1) {
            count = 1;
            for (var k = i + 1; k < nums.length; k++) {
                if (nums[k] != 0) {
                    count++;
                }

                if (nums[k] == 0 || k == nums.length - 1) {
                    break;
                }
            }
            resArr.push(count);
        }
    }

    for (var k = 0; k < resArr.length; k++) {
        if (resArr[k] > maxRes) {
            maxRes = resArr[k];
        }
    }

    return maxRes;
};

